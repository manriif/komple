package komple.gradle.kmp

import org.gradle.api.DefaultTask
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.tasks.InputFile
import org.gradle.api.tasks.Optional
import org.gradle.api.tasks.PathSensitive
import org.gradle.api.tasks.PathSensitivity

/**
 * Task responsible for generating a `.def` file, such as expected by the Kotlin CInterop tool.
 */
public abstract class GenerateCInteropDefTask : DefaultTask() {

    /**
     * The input library file, can be set to null to not add a `staticLibraries` entry in the
     * generated `.def` file.
     * This should be only set to `null` under some special circumstances.
     */
    @get:InputFile
    @get:PathSensitive(PathSensitivity.RELATIVE)
    @get:Optional
    public abstract val libraryFile: RegularFileProperty
}